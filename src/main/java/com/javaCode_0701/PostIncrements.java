package com.javaCode_0701;

public class PostIncrements {

	public static void main(String[] args) {
		
		int i =1;
		++i;
		
		System.out.println(i);
		
		int j = ++i;
		
		System.out.println(j);
		System.out.println(i);
		
		j = ++i + ++i;
		
		System.out.println(j);
		System.out.println(i);
		
		

	}

}
