package com.javaCode_0703;

public class EvenOrOddNumbers {

	public static void main(String[] args) {
	
		
		int i = 35;
		int n = 60;
		int j = 50;
		int t = 87;
		
		
		if(i%2==0) {
			System.out.println("int i is even :"+i);
		}else {
			  System.out.println("int i is odd :"+i);
		}
		
		
		if(n%2==0) {
			System.out.println("int n is even :"+n);
		}else {
			  System.out.println("int n is odd :"+n);
		}
			
		
		if(j%2==0) {
			System.out.println("int j is even :"+j);
		}else {
			  System.out.println("int j is odd :"+j);
		}
		
		
		if(t%2==0) {
			System.out.println("int t is even :"+t);
		}else {
			  System.out.println("int t is odd :"+t);
		}
		
		
		
		
		
		
		
		

	}

}
