package com.javaCode_0703;

public class SmallestBetweenFourNumber {

	public static void main(String[] args) {
		
		
		int i = 30;
		double j = 20.17;		
		int t = 90;
		int n = 10000;
		
		
		if(i<j&&i<t&&i<n) {
			
			System.out.println("The smallest value is i :"+i);
		}else if (j<i&&j<t&&j<n) {
			
			System.out.println("The smallest value is j :"+j);
		}else if (t<i&&t<j&&t<n) {
			
			System.out.println("The smallest value is t :"+t);
		}else if (n<i&&n<t&&n<j) {
			
			System.out.println("The smallest value is n :"+n);
		}
			
				
				
				
		
	}

}
