package com.javaCode_0715;

public class MoreAboutForLoops {

	public static void main(String[]args) {
		//nested for loop
		
		for(int i =1; i<=5; i++) {
			
			for(int j =1; j<=2; j++) {
				
				System.out.print(j+" | "+ " j = ");
			}
			System.out.println("       ");
			System.out.print(i+" | "+" i = ");
			
		}
		
		
		
		
		
	}
	
}
