package com.javaCode_0715;

public class Pattern3 {

	public static void main(String[] args) {
		
		/*
		
		* * * * *
		* * * *
		* * *
		* * 
		*
		 make this pattern
		
		*/
		
		
		
		for (int i =1 ; i<=5; i++) {
			
			for(int j =5; j>=i; j--) {
				System.out.print(" * ");
				
			}
			System.out.println();
		}
		

		
		
	}

}
